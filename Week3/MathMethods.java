public class MathMethods {

    public static void main(String[] args) {

        System.out.println("Square root of 3:" + Math.sqrt(3));
        System.out.println("The cubic of 2:" + Math.pow(2, 3));
        System.out.println("The absolute value of -123:" + Math.abs(-123));
        System.out.println("Draw a random number between 0 and 1:" + Math.random());

    }

}
